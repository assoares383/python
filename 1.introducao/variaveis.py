# Inteiro
numero_inteiro1 = 3
numero_inteiro2 = 4
print("Inteiro 1 = ", numero_inteiro1)
print("Inteiro 2 = ", numero_inteiro2)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# Type()
print("Inteiro = ", type(numero_inteiro1))
print("Real com ponto flutuante = ", type(numero_real))

# Soma +
soma = numero_inteiro1 + numero_inteiro2
print("O resultado da soma eh: ", soma)

# Subtracao -
subtracao = numero_inteiro1 - numero_inteiro2
print("O resultado da subtracao eh: ", subtracao)

# Multiplacao *
multiplicacao = numero_inteiro1 * numero_inteiro2
print("O resultado da multiplicacao eh: ", multiplicacao)

# Divisao /
divisao = numero_inteiro1 / numero_inteiro2
print("O resultado da divisao eh: ", divisao)

# Modulo /
modulo = numero_inteiro1 % numero_inteiro2
print("O resultado da modulo eh: ", modulo)